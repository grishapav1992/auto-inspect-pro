const STORAGE_KEY = "report_drafts";
const COMPRESSION_CACHE_LIMIT = 200;
const SMALL_JPEG_DATA_URL_LENGTH = 200_000;
const COMPRESS_BATCH_SIZE = 4;
const YIELD_EVERY = 8;
const compressionCache = new Map<string, string>();

function getCachedCompression(dataUrl: string): string | null {
  const cached = compressionCache.get(dataUrl);
  if (!cached) return null;
  compressionCache.delete(dataUrl);
  compressionCache.set(dataUrl, cached);
  return cached;
}

function setCachedCompression(source: string, compressed: string): string {
  if (compressionCache.has(source)) compressionCache.delete(source);
  compressionCache.set(source, compressed);
  if (compressionCache.size > COMPRESSION_CACHE_LIMIT) {
    const oldestKey = compressionCache.keys().next().value;
    if (oldestKey) compressionCache.delete(oldestKey);
  }
  return compressed;
}

function yieldToBrowser(): Promise<void> {
  return new Promise((resolve) => {
    if (typeof window !== "undefined" && "requestAnimationFrame" in window) {
      window.requestAnimationFrame(() => resolve());
      return;
    }
    setTimeout(resolve, 0);
  });
}

/**
 * compressImage — сжимает base64-изображение до указанной ширины и качества JPEG.
 * Используется для уменьшения объёма данных перед сохранением в localStorage.
 * Не-изображения возвращаются без изменений.
 */
function compressImage(dataUrl: string, maxWidth = 800, quality = 0.6): Promise<string> {
  return new Promise((resolve) => {
    // IDB keys are tiny strings — no compression needed
    if (isIdbUrl(dataUrl)) {
      resolve(dataUrl);
      return;
    }
    const cached = getCachedCompression(dataUrl);
    if (cached) {
      resolve(cached);
      return;
    }
    if (!dataUrl.startsWith("data:image")) {
      resolve(dataUrl);
      return;
    }
    if (dataUrl.startsWith("data:image/jpeg") && dataUrl.length <= SMALL_JPEG_DATA_URL_LENGTH) {
      resolve(setCachedCompression(dataUrl, dataUrl));
      return;
    }
    const img = new Image();
    img.onload = () => {
      const canvas = document.createElement("canvas");
      let w = img.width;
      let h = img.height;
      if (w > maxWidth) {
        h = (h * maxWidth) / w;
        w = maxWidth;
      }
      canvas.width = w;
      canvas.height = h;
      const ctx = canvas.getContext("2d");
      ctx?.drawImage(img, 0, 0, w, h);
      resolve(setCachedCompression(dataUrl, canvas.toDataURL("image/jpeg", quality)));
    };
    img.onerror = () => resolve(setCachedCompression(dataUrl, dataUrl));
    img.src = dataUrl;
  });
}

async function compressUrlList(urls: string[]): Promise<string[]> {
  const result = [...urls];
  for (let start = 0; start < urls.length; start += COMPRESS_BATCH_SIZE) {
    const batch = urls.slice(start, start + COMPRESS_BATCH_SIZE);
    const compressedBatch = await Promise.all(batch.map((url) => compressImage(url)));
    compressedBatch.forEach((compressed, index) => {
      result[start + index] = compressed;
    });
    if (start + COMPRESS_BATCH_SIZE < urls.length && (start + COMPRESS_BATCH_SIZE) % YIELD_EVERY === 0) {
      await yieldToBrowser();
    }
  }
  return result;
}

async function compressInspectionRecord(
  inspections: Record<string, PartInspection>,
): Promise<Record<string, PartInspection>> {
  const output: Record<string, PartInspection> = {};
  let processed = 0;

  for (const [key, inspection] of Object.entries(inspections)) {
    if (!inspection) continue;

    const nextInspection: PartInspection = {
      ...inspection,
      photos: await compressUrlList(inspection.photos ?? []),
    };

    if (inspection.tagPhotos) {
      const tagPhotosEntries = await Promise.all(
        Object.entries(inspection.tagPhotos).map(async ([tag, urls]) => [tag, await compressUrlList(urls)] as const),
      );
      nextInspection.tagPhotos = Object.fromEntries(tagPhotosEntries);
    }

    output[key] = nextInspection;
    processed += 1;
    if (processed % YIELD_EVERY === 0) {
      await yieldToBrowser();
    }
  }

  return output;
}

/**
 * compressDraftPhotos — рекурсивно сжимает все фотографии внутри черновика:
 * массивы фото каждого раздела и вложенные фото в инспекциях (photos, tagPhotos).
 * Возвращает новый объект черновика с уменьшенными изображениями.
 */
async function compressDraftPhotos(draft: ReportDraft): Promise<ReportDraft> {
  const result = { ...draft };
  result.inspectionPhotos = await compressUrlList(result.inspectionPhotos);
  result.underhoodPhotos = await compressUrlList(result.underhoodPhotos);
  result.bodyPhotos = await compressUrlList(result.bodyPhotos);
  result.glassPhotos = await compressUrlList(result.glassPhotos);
  result.interiorPhotos = await compressUrlList(result.interiorPhotos);
  result.wheelsPhotos = await compressUrlList(result.wheelsPhotos);
  result.tdPhotos = await compressUrlList(result.tdPhotos);

  result.inspections = await compressInspectionRecord(result.inspections);
  result.bodyStructuralInspections = await compressInspectionRecord(result.bodyStructuralInspections);
  result.bodyUndercarriageInspections = await compressInspectionRecord(result.bodyUndercarriageInspections);
  result.glassInspections = await compressInspectionRecord(result.glassInspections);

  return result;
}

/**
 * loadDrafts — загружает массив черновиков отчётов из localStorage.
 * При ошибке парсинга возвращает пустой массив.
 */
export function loadDrafts(): ReportDraft[] {
  try {
    const raw = localStorage.getItem(STORAGE_KEY);
    return raw ? JSON.parse(raw) : [];
  } catch {
    return [];
  }
}

/**
 * convertDraftUrlsToKeys — заменяет objectUrl → idb://key во всех фото-полях черновика
 * перед сохранением в localStorage.
 */
function convertUrlsToKeys(urls: string[]): string[] {
  return urls.map((u) => getIdbKey(u));
}

function convertInspectionUrlsToKeys(
  inspections: Record<string, PartInspection>,
): Record<string, PartInspection> {
  const output: Record<string, PartInspection> = {};
  for (const [key, insp] of Object.entries(inspections)) {
    if (!insp) continue;
    const next: PartInspection = {
      ...insp,
      photos: convertUrlsToKeys(insp.photos ?? []),
    };
    if (insp.tagPhotos) {
      next.tagPhotos = Object.fromEntries(
        Object.entries(insp.tagPhotos).map(([tag, urls]) => [tag, convertUrlsToKeys(urls)]),
      );
    }
    output[key] = next;
  }
  return output;
}

function convertDraftUrlsToKeys(draft: ReportDraft): ReportDraft {
  return {
    ...draft,
    inspectionPhotos: convertUrlsToKeys(draft.inspectionPhotos),
    underhoodPhotos: convertUrlsToKeys(draft.underhoodPhotos),
    bodyPhotos: convertUrlsToKeys(draft.bodyPhotos),
    glassPhotos: convertUrlsToKeys(draft.glassPhotos),
    interiorPhotos: convertUrlsToKeys(draft.interiorPhotos),
    wheelsPhotos: convertUrlsToKeys(draft.wheelsPhotos),
    tdPhotos: convertUrlsToKeys(draft.tdPhotos),
    inspections: convertInspectionUrlsToKeys(draft.inspections),
    bodyStructuralInspections: convertInspectionUrlsToKeys(draft.bodyStructuralInspections),
    bodyUndercarriageInspections: convertInspectionUrlsToKeys(draft.bodyUndercarriageInspections),
    glassInspections: convertInspectionUrlsToKeys(draft.glassInspections),
    mediaFiles: draft.mediaFiles.map((m) => ({
      ...m,
      url: getIdbKey(m.url),
      ...(m.children ? { children: m.children.map((c: any) => ({ ...c, url: getIdbKey(c.url) })) } : {}),
    })),
  };
}

/**
 * saveDraft — конвертирует objectUrl→idb-ключи, сжимает оставшиеся base64,
 * и сохраняет черновик в localStorage.
 */
export async function saveDraft(draft: ReportDraft): Promise<void> {
  const keyed = convertDraftUrlsToKeys(draft);
  const compressed = await compressDraftPhotos(keyed);
  const drafts = loadDrafts();
  const existingByVin = compressed.vin
    ? drafts.find((d) => d.id !== compressed.id && d.vin === compressed.vin)
    : null;

  const updated = existingByVin
    ? drafts.map((d) =>
        d.id === existingByVin.id
          ? { ...compressed, id: existingByVin.id, updatedAt: new Date().toISOString().slice(0, 10) }
          : d
      )
    : (() => {
        const filtered = drafts.filter((d) => d.id !== compressed.id);
        filtered.unshift({ ...compressed, updatedAt: new Date().toISOString().slice(0, 10) });
        return filtered;
      })();

  try {
    localStorage.setItem(STORAGE_KEY, JSON.stringify(updated));
  } catch (e) {
    console.warn("Draft save failed (storage full), trying to free space...", e);
    const trimmed = updated.slice(0, Math.max(1, updated.length - 1));
    try {
      localStorage.setItem(STORAGE_KEY, JSON.stringify(trimmed));
    } catch {
      console.error("Cannot save draft — localStorage is full.");
      throw e;
    }
  }
}

/**
 * deleteDraft — удаляет черновик по id из localStorage и очищает связанные blob-ы в IndexedDB.
 */
export function deleteDraft(id: string): void {
  const drafts = loadDrafts();
  const target = drafts.find((d) => d.id === id);
  if (target) {
    const keys = collectIdbKeys(target);
    if (keys.length > 0) {
      deleteBlobs(keys).catch((err) => console.warn("Failed to delete IDB blobs:", err));
    }
  }
  localStorage.setItem(STORAGE_KEY, JSON.stringify(drafts.filter((d) => d.id !== id)));
}

/**
 * loadDraftWithMedia — загружает черновик и резолвит все idb:// ключи в objectUrl.
 * Используется при восстановлении черновика в CreateReport.
 */
export async function loadDraftWithMedia(draft: ReportDraft): Promise<ReportDraft> {
  const resolveArr = async (urls: string[]) => resolveMediaUrls(urls);

  const resolveInspections = async (rec: Record<string, PartInspection>): Promise<Record<string, PartInspection>> => {
    const out: Record<string, PartInspection> = {};
    for (const [key, insp] of Object.entries(rec)) {
      if (!insp) continue;
      const next: PartInspection = {
        ...insp,
        photos: await resolveArr(insp.photos ?? []),
      };
      if (insp.tagPhotos) {
        const entries = await Promise.all(
          Object.entries(insp.tagPhotos).map(async ([tag, urls]) => [tag, await resolveArr(urls)] as const),
        );
        next.tagPhotos = Object.fromEntries(entries);
      }
      out[key] = next;
    }
    return out;
  };

  const result: ReportDraft = {
    ...draft,
    inspectionPhotos: await resolveArr(draft.inspectionPhotos),
    underhoodPhotos: await resolveArr(draft.underhoodPhotos),
    bodyPhotos: await resolveArr(draft.bodyPhotos),
    glassPhotos: await resolveArr(draft.glassPhotos),
    interiorPhotos: await resolveArr(draft.interiorPhotos),
    wheelsPhotos: await resolveArr(draft.wheelsPhotos),
    tdPhotos: await resolveArr(draft.tdPhotos),
    inspections: await resolveInspections(draft.inspections),
    bodyStructuralInspections: await resolveInspections(draft.bodyStructuralInspections),
    bodyUndercarriageInspections: await resolveInspections(draft.bodyUndercarriageInspections),
    glassInspections: await resolveInspections(draft.glassInspections),
  };

  // Resolve media file URLs
  result.mediaFiles = await Promise.all(
    draft.mediaFiles.map(async (m) => {
      const resolved: any = { ...m, url: await resolveMediaUrls([m.url]).then((r) => r[0]) };
      if (m.children) {
        resolved.children = await Promise.all(
          m.children.map(async (c: any) => ({
            ...c,
            url: await resolveMediaUrls([c.url]).then((r) => r[0]),
          })),
        );
      }
      return resolved;
    }),
  );

  return result;
}

/**
 * getDraftDisplayInfo — возвращает отображаемое название и год для карточки черновика.
 * Приоритет: бренд+модель → VIN → госномер → «Новый отчёт».
 */
export function getDraftDisplayInfo(draft: ReportDraft) {
  const brand = draft.carResult?.brand.name ?? "";
  const model = draft.carResult?.model.model ?? "";
  const label = brand && model ? `${brand} ${model}` : draft.vin || draft.plate || "Новый отчёт";
  const year = draft.carResult?.generation?.generation ?? null;
  return { label, year };
}
